<p style="color: #000">This quasi-judiciary office therefore has to point out the violation of the rights by different agencies, so that what the government seeks to achieve through pro-people steps of a democratically elected government are not negated by acts of omission and commission of bureaucrats at different levels. It also has to monitor that the benefits go to the right persons. Often the State Commissioner has to take suomotu cognizance of the violation and issue notices to the erring parties.The SCPD also takes steps to increase awareness among stakeholders and service providers which helps in implementation of the Act.</p>

  
