<div id="dc">
	<h3>
		Guidelines for issue of Disability Certificates</h3>
</div>
<p>
	The Persons with Disabilities (Equal Opportunities, Protection of Rights and Full Participation) Rules, 1996 provide the broad guidelines for issue of the disability certificates. The Rules lay down that a Medical Board, duly constituted by the Central and the State Government, shall issue a Disability Certificate. The certificate issued by the Medical Board makes a person eligible to apply for facilities, concessions and benefits admissible under schemes of the Governments or Non-Governmental Organisations, subject to such conditions as the Central or the State Government may impose.</p>
<p>
	The guidelines issued by the Government for simplification of the procedures for issue of disability certificates are:-</p>
<ol>
	<li>
		Particular day(s) in a week/month for issue of certificates may be fixed.</li>
	<li>
		Camps may be held for issuing disability certificate at the taluka/block level.</li>
	<li>
		Camps with other programmes such as rural development and poverty alleviation may be linked up.</li>
	<li>
		Specific time frame may be prescribed for issue of disability certificate may be given.</li>
	<li>
		Difficulties are being faced in issue of disability certificate for persons with mental retardation due to non-availability of psychiatrist / clinical psychologist / pediatrician in the Medical Board. Private practitioners of repute may be co-opted in the Medical Board.</li>
	<li>
		No fee should be charged from the persons with disabilities for issue of disability certificate.</li>
</ol>
<p>
	For issue of disability certificate to students with disabilities, the guidelines are: -</p>
<ol>
	<li>
		The responsibility for arranging the issue of the certificate has to be of the principals/headmasters of the schools.</li>
	<li>
		On the written request of school authorities, the District Medical Board should visit the school for evaluation of the disability of the student and issue certificate as per the notified guidelines and format of certificate.</li>
	<li>
		In case the Medical Board is of the view that the evaluation of disability of a particular student can be done only after carrying out certain tests with the equipments available in the District Hospital and therefore, the student should brought to the Hospital, the school authority shall make arrangement for the visit. The expenditure will be borne by the Education Department of the State Government / UT Administration.</li>
	<li>
		No fee should be charged from the disabled students for issue of disability certificate.</li>
	<li>
		The certificate may be issued before completion of the schooling of the disabled student along with their School Leaving Certificate or as and when required.</li>
</ol>
