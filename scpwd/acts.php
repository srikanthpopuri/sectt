<div id="345">
	<h3>
		Guidelines and Space Standards for Barrier Free Built Environment for Disabled and Elderly Persons</h3>
</div>
<p>
	Barrier-free environment enables people with disabilities to move about safely and freely, and use the facilities within the built environment. The goal of barrier free design is to provide an environment that supports the independent functioning of individuals so that they can participate without assistance, in every day activities. Therefore, to the maximum extent possible, buildings / places / transportation systems for public use will be made barrier free. Central Public Works Department under the Ministry of Urban Development has issued guidelines in this regard.</p>
<p>
	<a href="http://disabilityaffairs.gov.in/upload/uploadfiles/files/glinecpwd.pdf" target="_blank" title="View Complete Details of Guidelines and Space Standards for Barrier Free Built Environment for Disabled and Elderly Persons, CPWD, M/o Urban Affairs &amp; Employment (New)">View Complete Details of Guidelines and Space Standards for Barrier Free Built Environment for Disabled and Elderly Persons, CPWD, M/o Urban Affairs &amp; Employment (New)&nbsp;<img src="http://disabilityaffairs.gov.in/images/pdf_icon.png" alt="Pdf"> size:( 0.33 MB)</a></p>
<h3>
