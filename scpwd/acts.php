<h3>
	1.Rights of Persons with Disability Act, 2016</h3>
<p>
	The Act has been enacted under Article 253 of the Constitution read with item No. 13 of the Union List (WORD - -1 ) . It gives effect to the proclamation on the full participation and equality of the persons with disabilities in the Asian &amp; Pacific Region and provides for their education, employment, creation of barrier free environment, social security, etc. The implementation of the Act requires a multi-sectoral collaborative approach by the appropriate governments, including various Central Ministries/Departments, States/Union Territories, local bodies.</p>
