             <h4 style="font-weight: 500; line-height: 30px;">According to World Health Organization that about 15% of the world's population lives with 
              some form of disability, of whom 2-4% experience significant difficulties in functioning. The global disability prevalence is 
              higher than previous WHO estimates, which date from the 1970s and suggested a figure of around 10%. This global estimate for 
              disability is on the rise due to population ageing and the rapid spread of chronic diseases, as well as improvements in the methodologies 
              used to measure disability.</h4>
                
              <h3 class="text-uppercasetext-theme-colored mt-0 mb-0 mt-sm-30">Prevalence of Disability in India </span></h3>
              
             <h4 style="font-weight: 500; line-height: 30px;">According to the Census 2001, there are 2.19 million people with disabilities in India 
             who constitute 2.13 % of the total population (Census 2001). Between the genders, the prevalence of disability was marginally higher among 
             males than among females with a prevalence rate for males at 2% and for women 1.5% (Census 2001; NSSO 2002). According to NSSO (2002), about 
             8.4% and 6.1% of the total estimated households in rural and urban India, respectively, reported to have at least one disabled person. 
             Among the rural residents, the prevalence of disability was 1.85% and that among the urban was 1.50%.</h4>
              
              <h2 class="text-uppercasetext-theme-colored mt-0 mb-0 mt-sm-30" style="font-size: 20px;">National Sample Survey Organization: </span></h2>
              
              <h4 style="font-weight: 500; line-height: 30px;">(NSSO) estimated that the number of persons with disabilities in India is 1.8% (49-90 million) of the Indian population (NSSO 2002), that 75% of persons with disabilities live in rural areas.The difference in estimates of Census (2001) and NSSO (2002) for different types of disabilities can be explained by the lack of universal definitions and criteria of disabilities used during the survey.</h4>
              
            
            <h3 class="text-uppercasetext-theme-colored mt-0 mb-0 mt-sm-30">Why to measure?</span></h3>
                 
                 
              <h4 style="font-weight: 500; line-height: 30px;">The World Report on Disability estimates that there are about one billion people with disabilities in the world. However, there is currently little data on disability, and so this figure is very uncertain. A key reason therefore to measure disability is to estimate how many people have disabilities both globally and within different countries. This information is important for advocacy and for planning programmes.It is also important to understand the situation of people with disabilities in different settings in order to best plan services that cater to their needs and provide equality of opportunity in accordance with the UN Convention on the Rights of Persons with Disabilities (UNCRPD).</h4>
            
            <h3 class="text-uppercasetext-theme-colored mt-0 mb-0 mt-sm-30">Magnitude of the problem worldwide</span></h3>
            
              <h4 style="font-weight: 500; line-height: 30px;">People who have disability have profound difficulties in functioning and participating in their daily activities. According to the World Health Survey, approximately 785 million (15.6%) persons 15 years and older are estimated to live with a disability. These estimates are higher than World Health Organization’s (WHO) previous estimates in 1970 which was around 10%. Of these, about 110 million people (2.2%) are estimated to have very significant difficulties in functioning. The Global Burden of Disease estimates that around 975 million (19.4%) persons live with some form of disability, of which around 190 million persons (3.8%) have severe disability. These severe disability conditions include quadriplegia, severe depression, or blindness. Only the Global Burden of Disease measures childhood disabilities (0–14 years), which is estimated to be 95 million (5.1%) children, of whom 13 million (0.7%) have severe disability.
</h4>
              
              
                <h3 class="text-uppercasetext-theme-colored mt-0 mb-0 mt-sm-30">Growing numbers of persons with disability
</span></h3>
            
              <h4 style="font-weight: 500; line-height: 30px;">The number of people having different disabilities is growing because of ageing populations and the increasing chronic health conditions such as diabetes, cardiovascular diseases, and mental illness. In addition, medical advances that preserve and prolong life create overwhelming demands for health and rehabilitation services. Types of disability in a particular country are influenced by the health conditions and trends in environmental and other factors – such as road traffic crashes, natural disasters, conflict, diet, and substance abuse. Though there is an increasing prevalence of disability worldwide, adequate attention has not been given in terms of evaluation, management and prevention of disability.
</h4>
              
              
