                <h3>How to Register a Complaint</h3>
                <p>
Please file your complaint along with disability certificate and supporting documents as mentioned in Rule 42 
of the Persons with Disabilities (Equal Opportunities, Protection of Rights and Full Participation) Rules, 1996 
(PwD Rules) by post or by hand or by online case filing system.
</p>
<h5>Rule 42 - Procedure to be followed by State Commissioner -</h5>
<p>
<strong>1.</strong> A complaint containing the following particulars shall be presented by the complainant in person or by his authorized 
agent to the State Commissioner for Persons with Disabilities or be sent by registered post addressed to the State Commissioner -
<br>
The name, description and the address of the complainant;<br>
The name, description and the address of the opposite party or parties, as the case may be,<br>
so far as they can be ascertained;<br>
The facts relating to complaint and when and where it arose;<br>
 Documents in support of the allegations contained in the complaint;<br>
The relief that the complainant claims.
</p>

<p>2. The State Commissioner on receipt of a complaint shall refer a copy of the complaint to the opposite party / parties mentioned in the complaint directing him to give his version of the case within a period of thirty days or such extended period not exceeding fifteen days as may be granted by the State Commissioner.</p>

<p>3. On the date of hearing or any other date to which hearing could be adjourned, it shall be obligatory on the parties or their agents to appear before the State Commissioner.</p>

<p>4. Where the complainant or his agent fails to appear before the State Commissioner on such days, the State Commissioner may in his discretion either dismiss the complaint on default or decide it on merits.</p>
<p>5. Where the opposite party or his authorized agent fails to appear on the date of hearing the State Commissioner may take such necessary action under section 63 of the Act as she / he deems fit for summoning and enforcing the attendance of the opposite party.</p>
<p>6. The State Commissioner may dispose of the complaint ex parte, if necessary.</p>
<p>7. The State Commissioner may on such terms as he deems fit and at any stage of the proceedings, adjourn the hearing of the complaint.</p>
<p>8. The complaint shall be decided, as far as possible, within a period of three months from the date of notice received by the opposite party.</p>

<p>Vide Notification No. GSR.2(E) dated 30.12.2009, Ministry of Social Justice & Empowerment, Government of India have amended the provision relating to issuance of disability certificate in PwD Rules, 1996. It requires that the medical authority shall issue a disability certificate in Form II or Form III or Form IV as applicable.
</p>

<h5>How to approach:</h5>
<p>You can approach to State Commissioner in one of the following ways:</p>
<p>1. In Person</p>
<p>2. Through a Representative</p>
<p>3. Through Registered Post</p>
<p>4. By E-mail</p>
<p>5. By Online Case Management System</p>





<h5>You need to give certain details:</h5>
<p><p>1. Your name, address & brief description of yourself and the nature of your disability</p>
<p>2. The name of the person (s) & organization(s) against whom you are making your complaint, together
with their address & details, so far as they can be ascertained.</p>
<p>3. The facts relating to the complaint</p>

<p>• What happened</p>
<p>• When it happened</p>
<p>• Where it happened</p>
<p>• What sort of relief you are claiming</p>

<h5>Documentation Required:</h5>
<p>You must be able to give Documents in support of the allegations contained in the complaint. You must enclose a copy of your Disability Certificate from a valid authority.</p>

 

<h5>What Happens Next:</h5>

   <p>Once the State Commissioner has received your complaint, a copy of the complaint will be referred to the appropriate party mentioned in the complaint directing him to give his version of the case.</p>
<p>If necessary the parties may be heard in person. If you (the complainant) or your authorized agent fails to appear on the date, the State Commissioner in his discretion may dismiss the complaint or decide on merits.</p>
<p>Where the opposite party or his authorized agent fails to appear on the date of hearing, the date of hearing, the Commissioner may take such necessary action under section 63 of the Act as he deems fit for summoning
and enforcing the attendance of the opposite party. The State Commissioner may dispose of the complaint exparte, if necessary. The State Commissioner may on such terms as he deems fit and at any stage of the proceedings, adjourn the hearing of the complaints.</p>

 
